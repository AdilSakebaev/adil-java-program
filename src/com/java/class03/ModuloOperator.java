package com.java.class03;

public class ModuloOperator {
    public static void main(String[] args) {

        System.out.println(11%10);
        System.out.println(51.%10);
        System.out.println(42%10);
        System.out.println(17.%10);

        int sum10=10;
        double double1=10.3d;
        short class10=113;
        System.out.println(sum10+class10+double1);

    }
}

